jQuery(document).ready(function ($) {
    (function initPlayPres() {
        $(".pres-cover-01").on("click", function () {
            $(this).fadeOut().siblings('.presentation-01').html(
                '<iframe src="https://docs.google.com/presentation/d/e/' + $(".pres-cover-01").data("pres") +
                 '/embed?start=false&loop=false&delayms=5000" allowfullscreen="true" mozallowfullscreen="true" webkitallowfullscreen="true" frameborder="0"></iframe>'
            );
        });
    })();
});
